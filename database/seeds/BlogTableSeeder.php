<p>The 21st&nbsp;century demands qualified human resources, generated by professionally managed institutions to produce superior results. Education as part of efforts to improve the quality of human resources also changes and follows the times. Therefore, professional teachers are required. To prepare a professional teacher must be prepared from the beginning, that is when they are still students (preservice teachers). During the course of lectures, preservice teachers of the University of Galuh Ciamis Biology are equipped with knowledge about Biology as content, pedagogy, and technology. These three types of knowledge interact and form new knowledge known as Technological Pedagogical Content Knowledge (TPACK) (Mishra and Koehler, 2006). TPACK which is owned by biology preservice teacher has an important role, because TPACK will influence the student how preservice teachers in teaching (Srisawasdi, 2012). Effective teachers not only know their subject matter but they also able to create a stimulating learning environment and apply pedagogical strategies, including&nbsp;technology-rich&nbsp;strategies that engage students while helping them improve their achievement (NCATE, 2010).</p>
<p>The TPACK framework was developed by Mishra and Koehler based on Lee Shulmans concept of PCK by adding technology. One form of TPACK application in learning is the use of technology in teaching certain materials. Technology is considered important to be integrated in learning to respond to the challenges of the 21st&nbsp;century. Teachers are not only required have the ability of PCK but also apply technology in learning, so technology, pedagogy and content has become a part of teacher education programs to prepare preservice teachers. Applying technology in its teaching process. This research was conducted in order to analyze TPACK development process in Biology preservices</p>
<p>teacher and to analyze how the training received during lecturing can support TPACK of Biology preservice teacher in animal physiology material (digestive system and circulatory system).</p>
<p><strong>2. Experimental Method</strong></p>
<p>This research is examining how preservice teachers develop and apply technological pedagogical content knowledge (TPACK) troughout their teacher preparation program. As part of the research plan, we constructed the Survey of Preservice Teacher&rsquo;s Knowledge of Teaching and Technology to collect data on preservice teacher&rsquo;s&nbsp;self-assessment&nbsp;of the seven knowledge domains within the TPACK framework. These knowledge domains include: technology knowledge (TK), content knowledge (CK), pedagogical knowledge (PK), pedagogical content knowledge (PCK), technological content knowledge (TCK), technological pedagogical knowledge (TPK), and finally, technological, pedagogical content knowledge (TPACK). This instrumen adopted from TPACK instrument developed by Widodo (2017).</p>
<p>Respondents who participated in this study are biology preservice teacher enrolled in a fourth semester in Biology Departement of Universitas Galuh Ciamis. The participants were students of the researcher&rsquo;s classes, the first class was designed to introduce participants the technology that can use to teach animal physiology, the second class was designed to introduce participants focus on pedagogical tasks, the third class is combine both technological andgical treatment, and the other class doen&rsquo;t get any treatment. At the beginning and end of course participant complete the TPACK instruments consist of questionaire and open ended question. The instrument constructed contained 35 items for measuring preservice teacherssself-assessments&nbsp; pedagoothe seven TPACK domains: 6 TK items, 5 CK items, 5 PK items, 6 PCK items, 4 TCK items, 4 TPK items, andf  5 TPACK items. For these 35 items, participants answered each question using the following&nbsp;five-level&nbsp;Likert scale:</p>
<p>1.Strongly disagree</p>
<p>2.Disagree</p>
<p>3.Neither agree nor disagree</p>
<p>4.Agree</p>
<p>5.Strongly agree.</p>
